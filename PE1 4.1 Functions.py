# Function to calculate and display the area of a square
def square(side_length):
    area = side_length * side_length
    print(f"The area of the square is {area} square units.")

# Function to calculate and display the area of a circle
def circle(radius):
    pi = 3.14
    area = pi * radius * radius
    print(f"The area of the circle is {area} square units.")

# Call the functions with sample values
square(4)     # Example: square with side length 4
circle(3)     # Example: circle with radius 3